<?php

class Photo
{
    private $idPhoto;
    private $titrePhoto;
    private $photo;

    public function __construct($idPhoto, $titrePhoto, $photo)
    {
        $this->idPhoto = $idPhoto;
        $this->titrePhoto = $titrePhoto;
        $this->photo = $photo;
    }

    // construction des getter and setter

    public function getIdPhoto()
    {
        return $this->idPhoto;
    }
    public function setIdPhoto($idPhoto)
    {
        $this->idPhoto = $idPhoto;
    }

    public function getTitrePhoto()
    {
        return $this->titrePhoto;
    }

    public function setTitrePhoto($titrePhoto)
    {
        $this->titrePhoto = $titrePhoto;
    }

    public function getPhoto()
    {
        return $this->photo;
    }
    public function setPhoto($Photo)
    {
        $this->Photo = $Photo;
    }
}